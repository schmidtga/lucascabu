            <p>Lucas “Cabu” is a self-taught multidisciplinary artist born in Santos - Brazil in 1983 and living in New York City since March 2020.</p>
            <p>“Cabu’s” work and research focus on the moving image, blending outdated analog technologies with digital softwares to create kinetic art objects, video performances and graphic art. Recurring themes that the artist explores include: utopia, branding, perspectivism, dynamics of control and decoloniality.</p>
            <p>An active member of a worldly connected punk/diy scene, "Cabu" has been producing design and music videos for bands for over 15 years as well as running the hyperactive label “Rolo Seco”, which released vinyl records, cassette tapes, large scale screen prints and other audiovisual objects of artists from different countries and backgrounds, all numbered in limited editions and sold at an affordable price.</p>
            <p>His work can be seen in exhibitions inside and outside Brazil, such as in art zines, album covers, posters and music videos.</p>


            <p>
                Lucas Valente também conhecido como “Cabu” nasceu na cidade de Santos em 1983 onde teve seus primeiros contatos com arte no final dos anos 90 dentro da prolífica cena hardcore/punk DIY de sua cidade. “Cabu” viveu em São Paulo por 17 anos antes de emigrar para Nova Iorque em março de 2020 onde reside e trabalha atualmente.
            </p>
            <p>
                Artista multidisciplinar autodidata, seu trabalho e pesquisa se concentram na imagem em movimento, mesclando tecnologias analógicas ultrapassadas junto a softwares digitais para criar objetos de arte cinética, vídeo-performances e arte gráfica. As diferentes facetas de seu trabalho visual se convergem na banda de psych-punk chamada Duplo, na qual canta, escreve letras, desenha gráficos e audiovisuais e os temas recorrentes que o artista explora incluem branding, minimalismo, perspectivismo, dinâmicas de controle, decolonialidade e utopia.
            </p>
            <p>
                Como membro ativo da cena musical punk/diy, nos últimos 20 anos produziu trabalhos de design e vídeo para bandas, além de co-dirigir, junto a seu sócio Mateus Mondini, o hiperativo selo/editor “Rolo Seco”, que lançou discos de vinil, fitas cassete, serigrafias e outros objetos audiovisuais híbridos de artistas de diferentes países e origens, todos numerados em edições limitadas e vendidos a preço acessível.
            </p>
            <p>
                Hoje como curador, Lucas propõe a criação de plataformas de expressão e colaborações entre artistas latino-americanos e norte-americanos com a intenção de criar pontes e apresentar uma visão não estereotipada da arte contemporânea latino-americana através do espaço cultural Selva baseado em NYC e idealizado pelo artista.
            </p>
